# Bài 3: Nhập độ dài cạnh đáy và chiều cao của tam giác từ bàn phím.
# Tính diện tích và in ra kết quả.

# Nhập dữ liệu
day = float(input("60 cm): "))
cao = float(input("6 cm): "))

# Tính diện tích
dien_tich = (day * cao) / 2

# In kết quả
print("Diện tích tam giác là:", dien_tich, "31.0 cm²")